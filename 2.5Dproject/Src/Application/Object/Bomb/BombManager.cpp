#include "BombManager.h"

#include "../../Scene/SceneManager.h"
#include "../Player/Player.h"
#include "Bomb.h"

// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// /////
// 初期化
// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// /////
void BombManager::Init()
{
	m_spawnTimer = 0;
}

// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// /////
// 更新
// ・一定間隔で爆弾を生成する
// ・鳥居の破壊数に応じて間隔を短くする
// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// /////
void BombManager::Update()
{
	//===================================================================
	// 演出中（必殺技など）はスキップ
	//===================================================================
	if (SceneManager::Instance().IsCutScene())
	{
		return;
	}

	//===================================================================
	// 鳥居の破壊数に応じて生成間隔を決める
	// 破壊数が増えるほど間隔が短くなる（爆弾が頻繁に落ちる）
	//===================================================================
	int breakCount = SceneManager::Instance().GetToriiBreakCount();

	// 破壊1つにつき100フレーム短縮（最低120フレーム）
	int currentInterval = BaseInterval - breakCount * 100;
	if (currentInterval < 120) { currentInterval = 120; }

	//===================================================================
	// タイマーを進めて間隔に達したら爆弾を生成
	//===================================================================
	m_spawnTimer++;

	if (m_spawnTimer >= currentInterval)
	{
		m_spawnTimer = 0;
		SpawnBomb();
	}
}

// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// /////
// 爆弾を生成する
// ・プレイヤーの現在位置を着弾地点にする
// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// /////
void BombManager::SpawnBomb()
{
	//===================================================================
	// プレイヤーを探して現在位置を取得
	//===================================================================
	Math::Vector3 playerPos = {};
	bool found = false;

	for (auto& obj : SceneManager::Instance().GetObjList())
	{
		std::shared_ptr<Player> player = std::dynamic_pointer_cast<Player>(obj);
		if (player == nullptr) { continue; }

		playerPos = player->GetPos();
		found = true;
		break;
	}

	// プレイヤーがいなければ生成しない
	if (!found) { return; }

	//===================================================================
	// 爆弾を生成してプレイヤーの位置を着弾地点に設定
	//===================================================================
	auto bomb = std::make_shared<Bomb>();
	bomb->SetTargetPos(playerPos);	// 着弾地点をセット
	bomb->Init();

	SceneManager::Instance().AddObject(bomb);
}