#pragma once

// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// /////
// 鳥居クラス
// ・鳥居モデルを描画する
// ・ワームホールアニメーションを表示する
// ・一定間隔で敵を生成する
// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// ///// /////
class Torii : public KdGameObject
{
public:

	Torii() {}
	~Torii() override {}

	void Init()			override;
	void Update()		override;
	void DrawLit()		override;
	void DrawBright()	override;

	//===================================================================
	// 最大出現数を設定する
	// GameScene::Init() から呼んで調整できるようにする
	//===================================================================
	void SetMaxSpawnCount(int _max) { m_maxSpawnCount = _max; }

private:

	// 敵を生成してシーンに追加する
	void SpawnEnemy();

	//===================================================================
	// 鳥居モデル
	//===================================================================
	std::shared_ptr<KdModelData> m_spModel;

	//===================================================================
	// ワームホールアニメーション
	//===================================================================

	// 板ポリゴン
	std::shared_ptr<KdSquarePolygon> m_polygon;

	// アニメーションカウント
	float m_animeCnt = 0.0f;

	// アニメーション速度
	static constexpr float AnimeSpeed = 0.1f;

	// 総コマ数（横5コマ）
	static constexpr int FrameCount = 5;

	//===================================================================
	// 敵の生成関連
	//===================================================================

	// 生成タイマー（フレーム数）
	int m_spawnTimer = 0;

	// 生成間隔（フレーム数）
	// 60fps × 10秒 = 600フレーム
	static constexpr int SpawnInterval = 180;

	// 最大生成数（外部から変更可能）
	int m_maxSpawnCount = 20;

	//===================================================================
	// 発光関連
	// 敵が出現するタイミングで鳥居を光らせる
	//===================================================================

	// 発光タイマー（フレーム数）
	int m_brightTimer = 0;

	// 発光時間（フレーム数）
	static constexpr int BrightTime = 60;

	// 発光の強さ
	static constexpr float BrightIntensity = 3.0f;
};